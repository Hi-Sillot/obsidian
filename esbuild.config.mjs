import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";
import vuePlugin from "esbuild-plugin-vue3";
import fs from "fs";
import path from "path";

const prod = (process.argv[2] === "production");
const distDir = "dist";

function getBanner() {
	return {
		js: `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`,
	};
}

function mergeCSSModules() {
	const modulesDir = "src/styles/modules";
	const moduleFiles = [
		"components.css",
		"enhancements.css",
		"charts.css",
		"misc.css"
	];

	let mergedCSS = '';
	for (const file of moduleFiles) {
		const filePath = path.join(modulesDir, file);
		if (fs.existsSync(filePath)) {
			const content = fs.readFileSync(filePath, 'utf-8');
			mergedCSS += `/* ============================================\n   Module: ${file}\n   ============================================ */\n\n${content}\n\n`;
			console.log(`[css-merge] Merged: ${file}`);
		} else {
			console.warn(`[css-merge] Module not found: ${file}`);
		}
	}

	const outputPath = "src/styles/styles.css";
	fs.writeFileSync(outputPath, mergedCSS.trim() + '\n');
	console.log(`[css-merge] Generated: ${outputPath} (${(mergedCSS.length / 1024).toFixed(1)} KB)`);
	return outputPath;
}

function copyReleaseFiles() {
	if (!fs.existsSync(distDir)) {
		fs.mkdirSync(distDir, { recursive: true });
	}

	// 合并插件自定义样式模块
	const cssSource = mergeCSSModules();

	// 检查 esbuild 是否提取了 TDesign 的 CSS
	let finalCSS = fs.readFileSync(cssSource, 'utf-8');
	const esbuildCSS = path.join(distDir, 'main.css');
	if (fs.existsSync(esbuildCSS)) {
		const tdesignCSS = fs.readFileSync(esbuildCSS, 'utf-8');
		finalCSS = `/* ============================================
   TDesign Mobile Vue (auto-extracted by esbuild)
   ============================================ */\n\n${tdesignCSS}\n\n${finalCSS}`;
		console.log(`[css] Merged TDesign CSS from esbuild output (${(tdesignCSS.length / 1024).toFixed(1)} KB)`);
	}

	// 写入最终的 styles.css
	const outputPath = path.join(distDir, 'styles.css');
	fs.writeFileSync(outputPath, finalCSS);
	console.log(`[css] Generated: ${outputPath} (${(finalCSS.length / 1024).toFixed(1)} KB)`);

	// 清理 esbuild 提取的临时 CSS 文件
	if (fs.existsSync(esbuildCSS)) {
		fs.unlinkSync(esbuildCSS);
		console.log(`[css] Cleaned up temporary file: main.css`);
	}

	const filesToCopy = [
		{ src: "manifest.json", dest: path.join(distDir, "manifest.json") },
	];

	for (const { src, dest } of filesToCopy) {
		if (fs.existsSync(src)) {
			fs.copyFileSync(src, dest);
			console.log(`[copy] ${src} → ${dest}`);
		} else {
			console.warn(`[copy] ${src} not found, skipping`);
		}
	}
}

const context = await esbuild.context({
	banner: getBanner(),
	entryPoints: ["src/main.ts"],
	bundle: true,
	plugins: [vuePlugin()],
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtins],
	format: "cjs",
	target: "es2018",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outdir: distDir,
	entryNames: "main",
	minify: prod,
});

if (prod) {
	await context.rebuild();
	copyReleaseFiles();
	await context.dispose();
	process.exit(0);
} else {
	copyReleaseFiles();
	await context.watch();
}
